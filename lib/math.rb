def addition(num1, num2)
  return num1 + num2
end

def subtraction(num1, num2)
  dif = num1 - num2
  return dif 
end

def division(num1, num2)
  quotient = num1 / num2
  return quotient
end

def multiplication(num1, num2)
  product = num1 * num2
  return product 
end

def modulo(num1, num2)
  modulo = num1 % num2
  return modulo
end

def square_root(num)
  root = Math.sqrt(num)
  return root 
end