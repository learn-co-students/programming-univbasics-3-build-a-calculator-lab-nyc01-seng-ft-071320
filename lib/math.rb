def addition(num1, num2)
  adds = num1 + num2
  adds
end

def subtraction(num1, num2)
  subtracts = num1 - num2
  subtracts
end

def division(num1, num2)
  divides = num1 / num2
  divides
end

def multiplication(num1, num2)
  multiplies = num1 * num2
  multiplies
end

def modulo(num1, num2)
  modulate = num1 % num2
  modulate
end

def square_root(num)
  square = Math.sqrt(num)
  square
end
